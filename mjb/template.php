/**
 * Override or insert variables into the page template.
 */
function mjb_process_page(&$variables) {

	/**
	* SEARCH BLOCK
	*/
  	$variables['search_box'] = drupal_render(drupal_get_form('search_form'));

    // Add information about the number of sidebars.
    // *We don't check first sidebar because it places in "navbar sidebar"
    if (!empty($variables['page']['sidebar_second'])) {
        $variables['content_column_class'] = ' class="col-lg-8"';
    }
    else {
        $variables['content_column_class'] = ' class="col-lg-12"';
    }

}